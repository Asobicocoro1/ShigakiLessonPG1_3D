/*
 ### アニメーションクリップのインスペクター詳細

Unityのアニメーターウィンドウでアニメーションクリップを選択すると、そのクリップの詳細設定をインスペクターで確認および変更できます。以下は、そのインスペクターの各項目の詳細な解説です。

1. **名前 (Name):**
   - アニメーションクリップの名前です。この名前は、アニメーターコントローラーで識別されます。

2. **サンプルレート (Sample Rate):**
   - アニメーションのサンプルレートを設定します。通常は秒あたりのフレーム数 (FPS) で指定されます。例えば、`30` なら30FPSです。

3. **ループタイム (Loop Time):**
   - チェックを入れると、アニメーションが終了したときに自動的に最初に戻ってループ再生されます。ウォークサイクルやアイドルアニメーションなど、繰り返し再生する必要があるアニメーションに適しています。

4. **ループポーズ (Loop Pose):**
   - チェックを入れると、ループアニメーションの最後のフレームと最初のフレームの間でスムーズな遷移が行われるように補間されます。ループアニメーションの継ぎ目が滑らかになります。

5. **ルートトランスフォームの位置 (Root Transform Position):**
   - **ベイクイントポーズ (Bake Into Pose):** ルートトランスフォームの位置（移動）がアニメーションのポーズに焼き付けられます。
   - **元の位置を保持 (Based Upon Original):** アニメーションの元のルート位置を保持します。

6. **ルートトランスフォームの回転 (Root Transform Rotation):**
   - **ベイクイントポーズ (Bake Into Pose):** ルートトランスフォームの回転がアニメーションのポーズに焼き付けられます。
   - **元の回転を保持 (Based Upon Original):** アニメーションの元のルート回転を保持します。
   - **XZ (Original):** XZ平面上のルートトランスフォームの回転のみを保持します。
   - **Y (Original):** Y軸回りのルートトランスフォームの回転のみを保持します。

7. **ルートトランスフォームのスケール (Root Transform Scale):**
   - **ベイクイントポーズ (Bake Into Pose):** ルートトランスフォームのスケールがアニメーションのポーズに焼き付けられます。
   - **元のスケールを保持 (Based Upon Original):** アニメーションの元のルートスケールを保持します。

8. **イベント (Events):**
   - アニメーションの特定のフレームにカスタムイベントを追加できます。イベントはスクリプトから呼び出され、特定のアニメーションフレームでアクションを実行します。




### トランジションを選択したときのインスペクター詳細

アニメーターウィンドウでトランジションを選択すると、そのトランジションの詳細設定をインスペクターで確認および変更できます。以下は、そのインスペクターの各項目の詳細な解説です。

1. **名前 (Name):**
   - トランジションの名前です。通常、自動で設定されますが、必要に応じて変更できます。

2. **条件 (Conditions):**
   - トランジションが発生する条件を設定します。条件は、アニメーターコントローラーのパラメータに基づいて設定されます。
   - **パラメータ (Parameter):** トランジションのトリガーとなるパラメータを選択します。
   - **比較 (Comparison):** パラメータが特定の値に到達したときにトランジションが発生するように設定します。比較演算子（`==`, `!=`, `>`, `<`, `>=`, `<=`）を使用します。
   - **値 (Value):** トランジションが発生する際のパラメータの値を設定します。

3. **設定 (Settings):**
   - トランジションの詳細な設定を行います。
   - **遷移の時間 (Transition Duration):** 遷移が完了するまでの時間を設定します。値が小さいほど、遷移は迅速に行われます。
   - **開始オフセット (Transition Offset):** トランジションの開始位置をアニメーションの時間軸上で設定します。これにより、アニメーションの特定のポイントから遷移を開始できます。
   - **固定期間 (Fixed Duration):** チェックを入れると、遷移時間が固定されます。チェックを外すと、遷移時間はターゲットアニメーションの長さに対する割合として計算されます。

4. **インターポレーション (Interpolation):**
   - **ブレンド (Blend):** トランジションの補間方法を設定します。リニア、カーブベース、カスタムなどの補間方法を選択できます。
   - **クロスフェード (Crossfade):** 遷移中のクロスフェードの割合を設定します。これにより、スムーズな遷移が可能になります。

5. **ミュート (Mute):**
   - トランジションを一時的に無効化します。これにより、トランジションを削除せずに動作を確認することができます。

6. **Solo:**
   - 特定のトランジションのみを有効化し、他のトランジションを無効化します。デバッグ時に特定のトランジションの動作を確認するのに便利です。

これらの詳細を確認し、適切に設定することで、アニメーションの遷移を滑らかかつ意図した通りに制御できます。
